package com.osh.datamodel;

public class ItemMetaInfo {
	
    private long id;
	
	private int x;
	private int y;
	private boolean visible;
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public boolean isVisible() {
		return visible;
	}
	public void setVisible(boolean visible) {
		this.visible = visible;
	}
	
	

}
